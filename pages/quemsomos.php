
<h2>Quem Somos</h2>
<div class="main">
<p>      
    Sonho do casal de imigrantes espanhóis, que já não viam mais futuro na sua freguesia de salgados
    feitos no food-truck do Porto da Barra-Salvador. Procuraram então, uma alternativa de negócio.
    Depois de muita ajuda e esforço, em Janeiro de 2010, fundaram uma fabrica de rosquinhas em casa
    o casal e quatro filhos, foram firmando seu espaço.
    Sempre preocupados em servir bem, traziam sempre novidades e pedidos dos clientes.
    Pouco a pouco a Doces Sonhos foi ganhando a clientela da vizinhança e recuperando antigos
    clientes que antes consumiam seus salgados.
</p>
